import { NewsItem } from "@/data/news"

export function buildNewsArticle(item: NewsItem) {
  if (item.content && item.content.trim() && item.content.trim() !== item.summary.trim()) return item.content

  const title = item.title
  const category = item.category
  const isTutorial = category === "教程资源" || /教程|指南|全流程|手把手|入门|搭建|安装|配置/.test(title)
  const isProduct = category === "产品发布" || /发布|推出|升级|上线/.test(title)
  const isAnalysis = category === "深度解读" || /评测|对比|分析|为什么|趋势|选型/.test(title)

  if (isTutorial) {
    return `## 这件事适合谁

这篇内容适合想把「${title}」真正落地的人，尤其是没有太多技术基础、但希望按步骤完成一次可验证实践的用户。

## 开始前先准备

1. 准备一个常用邮箱或手机号，用来注册相关平台账号。
2. 准备一个具体任务，不要只说“我想试试 AI”。例如：做一个客服助手、总结一份 PDF、生成一篇小红书笔记、搭建一个知识库问答。
3. 如果涉及 API Key，先准备一个单独记录密钥的位置，不要把密钥发到公开聊天、公开仓库或截图里。

## 推荐执行步骤

第一步，先读清楚官方入口和账号要求。不要急着充值，先确认免费额度、地区限制、模型限制和是否需要额外插件。

第二步，用最小任务跑通一次。比如知识库类工具先上传 1 个文档，自动化类工具先串 2 个节点，编程类工具先修一个很小的页面问题。

第三步，检查输出。AI 生成的结果一定要人工看一遍，重点检查事实、数字、链接、权限和是否误删/误发内容。

第四步，保存模板。把能稳定复用的提示词、工作流、配置项保存下来，下次直接改变量，不要每次从零开始。

## 小白最容易踩的坑

- 任务太大：第一次就想做完整系统，容易失败。先做一个可完成的小闭环。
- 指令太虚：不要只写“帮我优化”，要写清楚目标、格式、字数、受众、限制条件。
- 不看成本：API、视频、绘图和语音类工具经常按量计费，正式使用前要设置预算。
- 不做备份：让 Agent 操作文件、表格、数据库前，先备份。

## 可行性判断

如果你的任务是高频、规则相对固定、允许人工复核，那么非常适合用这类 AI 工具。如果任务涉及法律、财务、医疗、合同签署或高风险操作，应保留人工审核环节。

## 本站建议

先把这篇内容当作一次实操清单，跑通后再去看原文或官方文档补细节。小白阶段最重要的不是知道所有功能，而是完成第一个能复用的 AI 工作流。`
  }

  if (isProduct) {
    return `## 发生了什么

${item.summary}

## 为什么值得关注

这类产品更新通常意味着 AI 工具正在从“能回答问题”走向“能完成任务”。对普通用户来说，真正重要的不是参数有多大，而是它能不能降低使用门槛、提升稳定性、减少配置成本。

## 对新手有什么影响

如果你刚开始用 AI，可以优先关注三个变化：

1. 是否更容易注册和使用。
2. 是否支持中文、文件、图片、长文本或联网。
3. 是否有免费额度，超出后怎么收费。

只要这三项变好，新手的上手成本就会明显降低。

## 可以怎么用

- 写作和总结：把长文档、会议记录、网页内容整理成清单。
- 办公提效：生成周报、PPT 大纲、邮件回复和表格分析。
- 学习辅助：让 AI 按你的水平解释概念，再出练习题。
- Agent 场景：把固定流程交给工具自动执行，但重要步骤保留确认。

## 需要注意

产品发布初期功能可能灰度开放，价格、额度、模型名称也可能调整。正式用于业务前，建议先用一两个真实案例测试稳定性，不要只看发布介绍。

## 本站判断

这条资讯的核心价值在于帮助你判断是否值得尝试。适合新手的标准很简单：能直接用、中文体验好、免费或低成本、出错后容易回退。`
  }

  if (isAnalysis) {
    return `## 核心结论

${item.summary}

## 判断框架

看 AI 工具或模型，不建议只看排行榜。更实用的判断方式是：

1. 能力：它在你的任务上是否真的做得更好。
2. 成本：价格、额度和隐藏成本是否能接受。
3. 稳定性：是否经常限流、断连或输出波动。
4. 易用性：新手是否能独立完成配置。
5. 替代性：如果它不可用，有没有备用工具。

## 适合场景

如果你做的是日常办公、写作、总结和轻量分析，优先选择便宜、稳定、中文友好的工具。如果你做的是复杂编程、长文档推理、多步 Agent 或高价值业务，再考虑更强但更贵的模型。

## 小白建议

不要为了“最强”盲目付费。先用免费或低价工具完成 80% 的任务，再把少数复杂任务交给高端模型。这样成本更低，效果也更稳定。

## 风险提醒

AI 圈更新非常快，模型能力和价格可能在几周内变化。把本文当作选型思路，而不是永久结论。`
  }

  return `## 摘要

${item.summary}

## 重点拆解

这条资讯值得关注的地方在于，它反映了 AI 工具正在继续降低使用门槛。对普通用户来说，真正有价值的是能不能把复杂能力变成日常可用的流程。

## 你可以怎么行动

1. 先判断它是否与你当前任务有关。
2. 如果有关，找一个小任务试用，不要直接迁移整个工作流。
3. 记录使用成本、输出质量和失败情况。
4. 如果连续三次都能稳定完成，再考虑长期使用。

## 本站建议

把 AI 当作一个需要调试的助手，而不是一次性买来的万能工具。越具体的场景，越容易获得稳定收益。`
}
