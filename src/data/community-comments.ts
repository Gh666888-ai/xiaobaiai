function pickCommenter(postId: string, index: number) {
  const seed = Array.from(postId).reduce((sum, char) => sum + char.charCodeAt(0), 0)
  return commenters[(seed + index * 3) % commenters.length]
}

export function getSeedCommunityComments(postId: string): SeedCommunityComment[] {
  const items = commentBank[postId] || [
    "这个帖子很适合新手先收藏，照着一步步试比只看概念靠谱。",
    "建议后续补一版实操截图，很多细节看图会更快理解。",
  ]

  return items.map((content, index) => {
    const commenter = pickCommenter(postId, index)
    return {
      id: `seed-${postId}-${index + 1}`,
      post_id: postId,
      author_name: commenter.name,
      author_xp: commenter.xp,
      content,
      created_at: `2026-05-${String(Math.min(7, 4 + index)).padStart(2, "0")}T${String(9 + index * 3).padStart(2, "0")}:18:00.000Z`,
    }
  })
}
