export function buildExpandedNews(existing: NewsItem[], target = 108): NewsItem[] {
  const result = [...existing]
  const used = new Set(result.map((item) => item.id))
  let index = 0

  while (result.length < target) {
    const [title, summaryTail] = tutorialTopics[index % tutorialTopics.length]
    const id = `xiaobai-guide-${index + 1}`
    if (!used.has(id)) {
      used.add(id)
      result.push({
        id,
        title,
        summary: `${summaryTail} 小白AI把它整理成站内可读教程，按准备、执行、检查、复用四个阶段讲清楚。`,
        content: buildGuideContent(title, summaryTail),
        url: "#",
        source: "小白AI编辑部",
        category: "教程资源",
        publishedAt: "2026-05-07",
        importance: 7 + (index % 3),
        isAutoGenerated: true,
      })
    }
    index += 1
  }

  return result
}

